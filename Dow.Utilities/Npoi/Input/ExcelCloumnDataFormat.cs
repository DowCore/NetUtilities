namespace Dow.Utilities.Npoi.Input
{
    public static class ExcelCloumnDataFormat
    {
        /// <summary>
        /// 常规
        /// </summary>
        public const string General           = "General";

        /// <summary>
        /// 数字
        /// </summary>
        public const string Numeral           = "0";

        /// <summary>
        /// 两位小数的数字
        /// </summary>
        public const string DecimalNumeral    = "0.00";

        /// <summary>
        /// 百分数
        /// </summary>
        public const string Percentage        = "0%";

        /// <summary>
        /// 百分数带两位小数
        /// </summary>
        public const string DecimalPercentile = "0.00%";

        /// <summary>
        /// 文本
        /// </summary>
        public const string Text              = "@";

        /// <summary>
        /// 日期
        /// </summary>
        public const string Date              = "yyyy-mm-dd";

        /// <summary>
        /// 时间
        /// </summary>
        public const string Time              = "hh:mm:ss";

        /// <summary>
        /// 日期带时间
        /// </summary>
        public const string DataTime          = "yyyy-mm-dd hh:mm:ss";
    }
}
